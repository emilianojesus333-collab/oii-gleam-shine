export function searchCatalog(
  query: string,
  filters: {
    muscleGroup?: string;
    equipment?: Equipment | "todos";
    difficulty?: Difficulty | "todos";
  }
): CatalogExercise[] {
  const q = query.toLowerCase().trim();

  return exerciseCatalog.filter((ex) => {
    const matchesQuery =
      !q ||
      ex.name.pt.toLowerCase().includes(q) ||
      ex.name.en.toLowerCase().includes(q) ||
      ex.muscleGroups.some((m) => m.toLowerCase().includes(q));

    const matchesMuscle =
      !filters.muscleGroup ||
      filters.muscleGroup === "Todos" ||
      ex.muscleGroups.includes(filters.muscleGroup) ||
      ex.secondaryMuscles.includes(filters.muscleGroup);

    const matchesEquipment =
      !filters.equipment ||
      filters.equipment === "todos" ||
      ex.equipment.includes(filters.equipment as Equipment);

    const matchesDifficulty =
      !filters.difficulty ||
      filters.difficulty === "todos" ||
      ex.difficulty === filters.difficulty;

    return matchesQuery && matchesMuscle && matchesEquipment && matchesDifficulty;
  });
}

export function getAlternatives(exerciseId: string): CatalogExercise[] {
  const ex = exerciseCatalog.find((e) => e.id === exerciseId);
  if (!ex) return [];
  return ex.alternatives
    .map((altId) => exerciseCatalog.find((e) => e.id === altId))
    .filter(Boolean) as CatalogExercise[];
}

export function getAlternativesForEquipment(
  exerciseId: string,
  availableEquipment: Equipment[]
): CatalogExercise[] {
  const alternatives = getAlternatives(exerciseId);
  if (availableEquipment.length === 0) return alternatives;
  return alternatives.filter((ex) =>
    ex.equipment.some((eq) => availableEquipment.includes(eq))
  );
}

