// Search function for finding foods
export const searchFoods = (query: string): FoodDatabaseItem[] => {
  const lowerQuery = query.toLowerCase();
  return foodDatabase.filter(food => 
    food.name.toLowerCase().includes(lowerQuery) ||
    food.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
};

// Get foods by category
export const getFoodsByCategory = (category: FoodDatabaseItem['category']): FoodDatabaseItem[] => {
  return foodDatabase.filter(food => food.category === category);
};

// Get pre-workout foods
export const getPreWorkoutFoods = (): FoodDatabaseItem[] => {
  return foodDatabase.filter(food => food.isPreWorkout);
};

// Get post-workout foods
export const getPostWorkoutFoods = (): FoodDatabaseItem[] => {
  return foodDatabase.filter(food => food.isPostWorkout);
};

